      <div class="play"><a name="play6"></a>
	    <h4>讓盤/大小過關交易</h4>
	    <div class="type">
		  <ul>
	        <li>讓盤過關是指在一場比賽中，其中一位球員提供讓<strong>盤(Set)</strong>作為讓賽條件，賽果<strong>比賽開打後最終完場之比賽結果</strong>為準。 </li>
	        <li>大/小(全場比賽進行之總<strong>局(Game)</strong>數) ：在指定球賽中當交易該場比賽的大/小(全場比賽進行之總<strong>局(Game)</strong>數)時，若比賽進行之總<strong>局(Game)</strong>數高於該場比賽進行的大/小局(Game)</strong>數則為大，若比賽進行之總<strong>局(Game)</strong>數低於該場比賽的大/小<strong>局(Game)</strong>數則為小。賽果以<strong>比賽開打後最終完場</strong>之比賽結果為準。</li>
	        <li>在「讓盤/大小過關」開放的每一場賽事中，客戶可交易其中一位玩法種類勝出。 </li>
	        <li>所有在「讓盤/大小過關」中公佈之讓<strong>盤(Set)</strong>條件及獲利率都可作過關交易（包括三關、四關等）。</li>
		  </ul>
