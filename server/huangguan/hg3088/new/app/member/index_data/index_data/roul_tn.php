      <h2><span>網球交易規則</span></h2>
      <ul>
        <li>所有網球交易皆以<strong>比賽開打後最終完場的比數</strong>為最終結果，若其後由任何體育紀律委員會重新裁決，而更改之賽果均不予以計算。</li>
        <li> 如因氣候、場地、球員因傷退賽等客觀因素，導致賽事中止，並延後再進行。若此賽事未在原定之比賽時間當天結束，則該場比賽之交易視為無效。</li>
        <li> 交易所有種類(除過關外) 之網球球賽在不足法定時間內被終止、因氣候、場地等客觀因素使比賽開打時間較原定比賽時間延遲超過十二小時、或被取消，則該場球賽的交易均視為無效，交易金額可獲全數退回。</li>
        <li> 交易過關的任何一場網球球賽，如在不足法定時間內被終止、或較原定比賽時間延遲超過十二小時或被取消，則該場球賽的交易均視為有效，並當作已過關論，惟獲利率概以「1」計算。</li>
        <li> 如球賽提前舉行，比賽開打後已接受之交易將視為無效。在球賽開始前所有已接受之交易均為有效。</li>
        <li> 網球四大公開賽（澳洲網球公開賽、美國網球公開賽、法國網球公開賽、溫布頓網球公開賽 ），賽果計算方式：
          <ul>
            <li>男子組比賽五盤中先贏三盤之球員，為勝方</li>
            <li>女子組比賽三盤中先贏二盤之球員，為勝方</li>
          </ul>
        </li>
      </ul>
