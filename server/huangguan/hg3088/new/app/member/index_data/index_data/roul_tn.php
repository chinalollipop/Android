      <div class="play"><a name="play2"></a>
	    <h4>滾球交易</h4>
	    <div class="type">
		  <h5>滾球讓盤</h5>
		  <ul>
	        <li>在指定比賽中，其中一位球員提供讓盤條件，賽果以<strong>開賽之後至最終完場之比賽結果</strong>為準。</li>
	        <li>勝出： 滾球讓盤是以 (比賽結果) (交易當時的比賽<strong>盤(Set)</strong>數)與交易當時的獲利率比對，作為勝出條件。</li>
	        <li>交易結果： 滾球讓盤交易結果是以符合勝出條件的獲利率作為計算交易結果的基準。</li>
		  </ul>
        </div><!-- type end -->
	    <div class="type">
		  <h5>滾球大小</h5>
		  <ul>
	        <li>在指定比賽中當交易該場比賽的大小(全場比賽進行之總<strong>局(Game)</strong>數)時，若比賽進行之總<strong>局(Game)</strong>數高於該場比賽的大小<strong>局(Game)</strong>數則為大，若比賽進行之總<strong>局(Game)</strong>數低於該場比賽的大小<strong>局(Game)</strong>數則為小。</li>
	        <li>勝出：滾球大小是以(比賽進行之總<strong>局(Game)</strong>數)與交易當時大/小的獲利率比對，作為勝出條件。</li>
	        <li>交易結果：滾球大小交易結果是以符合勝出條件的獲利率作為計算交易結果的基準。</li>
		  </ul>
        </div><!-- type end -->
      </div><!-- play end -->
      <div class="top"><a href="#top"><span>回最上層</span></a></div>
      <div class="play"><a name="play3"></a>
	    <h4>總局數交易<em>(比賽局數)</em></h4>
	    <div class="type">
		  <ul>
	        <li>單 / 雙：在指定比賽中，兩球員在<strong>比賽開打後最終完場</strong>的法定時間內全場比賽進行總<strong>局(Game)</strong>數的總數為「 單 」或 「 雙 」，賽果均以<strong>開賽之後至最終完場的比數</strong>為準。</li>
 	        <li>例：比賽進行總<strong>局(Game)</strong>數為31：即全場總<strong>局(Game)</strong>數為<strong>單數</strong></li>
 	        <li>例：比賽進行總<strong>局(Game)</strong>數為30：即全場總<strong>局(Game)</strong>數為<strong>雙數</strong></li>
		  </ul>
        </div><!-- type end -->
      </div><!-- play end -->
      <div class="top"><a href="#top"><span>回最上層</span></a></div>

      <div class="play"><a name="play4"></a>
	    <h4>波膽交易<em>(比賽盤數)</em></h4>
		<p>在指定比賽中，選定兩位球員勝負盤數的交易，賽果以<strong>比賽開打後最終完場</strong>之比賽結果為準。</p>
      </div><!-- play end -->

      <div class="play"><a name="play5"></a>
	    <h4>標準過關交易</h4>
	    <div class="type">
		  <ul>
	        <li>標準過關是指在一場比賽中，兩位球員以<strong>平手</strong>情況下比賽，賽果以<strong>比賽開打後最終完場</strong>之比賽結果為準。 </li>
	        <li> 在「標準過關」開放的每一場賽事中，客戶可交易其中一位球員勝出。 </li>
	        <li> 所有在「標準過關」中公佈之獲利率都可作過關交易（包括二關、三關、四關等）。</li>
		  </ul>
        </div><!-- type end -->
      </div><!-- play end -->
