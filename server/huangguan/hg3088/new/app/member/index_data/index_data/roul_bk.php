      <ul>
        <li>所有籃球交易皆以<strong>比賽開打後至少要超過之43分鐘至</strong>最終完場的比數為最終結果，若其後由任何體育紀律委員會重新裁決，而更改之賽果均不予以計算。</li>
        <li>交易所有種類(除過關外)之籃球球賽在不足法定時間內被終止、或較原定比賽時間延遲超過二十四小時或被取消，則該場球賽的交易均視為無效，交易金額可獲全數退回。</li>
        <li>交易過關的任何一場籃球球賽，如在不足法定時間內被終止、或較原定比賽時間延遲超過二十四小時或被取消，則該場球賽的交易均視為有效，並當作已過關論，惟獲利率概以「1」計算。</li>
        <li>如球賽提前舉行，則在球賽開始前所有已接受之交易均為有效。</li>
      </ul>
