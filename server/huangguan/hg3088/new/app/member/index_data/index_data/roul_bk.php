	    <h4>上/下半場交易</h4>
		<ul>
		  <li>上半場為該場賽事第一、二節之比數總合。</li>
	      <li>下半場為該場賽事第三、四節之比數總合 ( 含延長加時)。</li>
		</ul>
	    <div class="type">
		  <h5>半場讓分</h5>
		  <p class="p5">在指定比賽中，其中一隊球隊提供讓分作為比賽條件，賽果均以半場結束的比數為準。</p>
	    </div><!-- type end -->
	    <div class="type">
		  <h5>半場大/小</h5>
		  <ul>
	        <li>大/小(全場總得分數) ：在指定比賽中當交易該場比賽的大/小(全場總得分數)時，若總得分高於該場比賽的大/小分數則為大，若總得分低於該場比賽的大/小分數則為小。</li>
            <li>如賽事結果之總得分數恰好等於「大/小分數」，則該交易金額將獲全數退回，賽果均以半場結束的比數為準。</li>
		  </ul>
