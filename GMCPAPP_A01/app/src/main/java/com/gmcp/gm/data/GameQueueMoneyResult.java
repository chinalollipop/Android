package com.gmcp.gm.data;

public class GameQueueMoneyResult {
    /**
     * fFreeMoney : 0
     * sMsg : 查询成功
     * fTotalMoney : 0
     */

    private String fFreeMoney;
    private String sMsg;
    private String fTotalMoney;

    public String getFFreeMoney() {
        return fFreeMoney;
    }

    public void setFFreeMoney(String fFreeMoney) {
        this.fFreeMoney = fFreeMoney;
    }

    public String getSMsg() {
        return sMsg;
    }

    public void setSMsg(String sMsg) {
        this.sMsg = sMsg;
    }

    public String getFTotalMoney() {
        return fTotalMoney;
    }

    public void setFTotalMoney(String fTotalMoney) {
        this.fTotalMoney = fTotalMoney;
    }
}
